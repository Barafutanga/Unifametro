number = 1024       #declarando uma variável do tipo inteiro
floatnumber = 10.2  #declarando uma variável do tipo decimal
word = "Café"       #declarando uma variável do tipo texto (String)

# 'puts' é utilizado para imprimir na tela e pular a linha 
# '#{}' faz o trabalho do %d, %f, %s no Java
puts "#{number} -> #{number.class}" 
puts "#{floatnumber} -> #{floatnumber.class}" 
puts "#{word} -> #{word.class}"